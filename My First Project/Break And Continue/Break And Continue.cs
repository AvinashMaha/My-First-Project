using System;
using System.Collections.Generic;
using System.Text;

namespace My_First_Project.Break_And_Continue
{
    class Break_And_Continue
    {
        static void Main(String[] args)
        {
        for(int i = 1; i <= 10; i++)
            {
                if (i == 3)
                {
                    break;
                }
                Console.WriteLine(i);
            }



        }
    }
}
