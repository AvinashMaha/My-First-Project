using System;
using System.Collections.Generic;
using System.Text;

namespace My_First_Project.Loop_Study
{
    class DisplayLoops
    {
        static void Main(String[] args)
        {
            for (int i = 15; i <= 40; i++)
            {
                Console.WriteLine(i);
            }

            for (int j = 150; j >= 101; j--)
            {
                Console.WriteLine(j);
            }


            
        }
    }
}
